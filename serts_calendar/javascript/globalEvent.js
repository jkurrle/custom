function hideGlobalEvents()
  {
  var i;
  for(i=1;i<=1000;i++)
    {
    document.getElementById('globalEvent'+i).style.display='none';
    }
  }
function showGlobalEvents()
  {
  var i;
  for(i=1;i<=1000;i++)
    {
    document.getElementById('globalEvent'+i).style.display='block';
    }
  }
